package com.itwillbs.domain;

public class CompanyDTO {
	
   private String companyId;		// 아이디
   private String companyPass;		// 비밀번호
   private String companyName;		// 회사명
   private String companyEmail;		// 회사 이메일
   private String companyTel;		// 회사 전화번호
   private String companyAddress;   // 회사 주소
   private String companyNum;		// 사업자등록번호
   
public String getCompanyId() {
	return companyId;
}
public void setCompanyId(String companyId) {
	this.companyId = companyId;
}
public String getCompanyPass() {
	return companyPass;
}
public void setCompanyPass(String companyPass) {
	this.companyPass = companyPass;
}
public String getCompanyName() {
	return companyName;
}
public void setCompanyName(String companyName) {
	this.companyName = companyName;
}
public String getCompanyEmail() {
	return companyEmail;
}
public void setCompanyEmail(String companyEmail) {
	this.companyEmail = companyEmail;
}
public String getCompanyTel() {
	return companyTel;
}
public void setCompanyTel(String companyTel) {
	this.companyTel = companyTel;
}
public String getCompanyAddress() {
	return companyAddress;
}
public void setCompanyAddress(String companyAddress) {
	this.companyAddress = companyAddress;
}
public String getCompanyNum() {
	return companyNum;
}
public void setCompanyNum(String companyNum) {
	this.companyNum = companyNum;
}
   
	   
	   
	   
	   
}
